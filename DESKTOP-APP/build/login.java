    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        try {
    		
 //---------------Get registered admin list from the server-------
    		
		this.adminList = sensor.getAdmins();
		
	} catch (RemoteException e) {
		// TODO Auto-generated catch block
		e.printStackTrace();
	}
       
    	
        String user = username.getText();
        String pwd = password.getText().toString();
        
        
 //-------check provided details are correct----------------
        
        int check = 0;
        for(Admin admin:adminList) {
        	System.out.println("ADmin");
        	if(admin.getUserName().equals(user) && admin.getPassword().equals(pwd)) {
        		
        		   check = 1;
        		   client.setLoginStatus(true);
        		   addUi =  new addsensorui1 ();
        	       addUi.setVisible(true);
        	       addUi.getServer(sensor);
        		
        	}
        }
      
        if(check == 0) {
        	username.setText("");
        	password.setText("");
        	 JOptionPane.showMessageDialog(this, "User name or password is incorrect");
        }else if(check == 1) {
        	
        	
        	this.setVisible(false);
        }
       
      
    }//GEN-LAST:event_loginbtnActionPerformed

