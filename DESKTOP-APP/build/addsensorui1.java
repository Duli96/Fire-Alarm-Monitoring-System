    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) throws MalformedURLException {//GEN-FIRST:event_addbtnActionPerformed
        
    	
    	Sensor newSensor = new Sensor();
    	
        //-----check input fields are empty-------
        
        if(floorno2.getText().equals("") || roomno.getText().equals("") || sid.getText().equals("")  ) {
        	
        	JOptionPane.showMessageDialog(this, "Please fill all the fields");
        	
        }else {
        //----get inputs from input fields-----------
        
        String id = sid.getText();
    	Integer number = new Integer(floorno2.getText());
    	int floor = number;
    	String room = roomno.getText();
    	
    	newSensor.setId(id);
    	newSensor.setFloorNo(floor);
    	newSensor.setRoomNo(room);
    	
    	//----check  if the sensor is already registered------
    	
    	int numb = 0;
    	try {
			sensors = sensor.getSensors();
		} catch (RemoteException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
    	
    	for(Sensor sen : sensors) {
    		
    		if(sen.getId().equals(id)) {
    			
    			numb = 1;
    		}
    	}
    	
    	if(numb == 1) {
        	
    		JOptionPane.showMessageDialog(this, "Sensor has already registered");
    		
        	}else if(numb == 0) {
        		try {
        			
            		
    				sensor.addSensor(newSensor);
    				this.setVisible(false);
    			
    		} catch (RemoteException e) {
    			// TODO Auto-generated catch block
    			e.printStackTrace();
    		} catch (Exception e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
        		
        	}
    	
    	
    	
        }
    }//GEN-LAST:event_addbtnActionPerformed

